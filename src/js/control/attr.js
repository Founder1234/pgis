goog.provide('Ez.controls.Attr');

Ez.controls.Attr = function(element) {
    element.className += " ol-unselectable ol-control";
    goog.base(this, {
        element: element
    });
}
goog.inherits(Ez.controls.Attr, ol.control.Control);
